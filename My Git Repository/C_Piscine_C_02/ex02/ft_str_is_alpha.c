#include <stdio.h>

int	is_alpha(char a)
{
	return ((a >= 'a' && a <= 'z') || (a >= 'A' && a <= 'Z'));
}

int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!is_alpha (str[i]))
			return (0);
		i++;
	}
	return (1);
}
